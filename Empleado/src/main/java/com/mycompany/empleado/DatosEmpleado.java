    private void BTNGuardarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNGuardarEmpleadoActionPerformed
       try {
        int id = Integer.parseInt(TXTIDEmpleado.getText());
        String nombre = TXTNombreEmpleado.getText();
        String apellido = TXTApellidoEmpleado.getText();
        double salario = Double.parseDouble(TXTSalario.getText());
        Date fechaInicio = jDateChooser1.getDate(); // fecha inicio contrato

        String tipo = (String) JCTipoEmpleado.getSelectedItem();

        if (tipo.equals("Empleado Permanente")) {
            String beneficio = TXTBeneficio.getText();

            EmpleadoPermanente emp = new EmpleadoPermanente(
                    id, nombre, apellido, fechaInicio, salario, beneficio);

            empleadoP.add(emp);
            javax.swing.JOptionPane.showMessageDialog(this, "Empleado permanente guardado");
        } else {
            Date fechaFin = JCalendarFinContato.getDate();
            int horas = Integer.parseInt(TXTHorasTrabajadas.getText());
            double valorHora = Double.parseDouble(TXTValorHora.getText());

            EmpleadoTemporal emp = new EmpleadoTemporal(
                    id, nombre, apellido, fechaInicio, salario, fechaFin, horas, valorHora);

            empleadoT.add(emp);
            javax.swing.JOptionPane.showMessageDialog(this, "Empleado temporal guardado");
        }

        // Limpiar campos y deshabilitar el botón hasta nuevo registro
        limpiarCampos();
        BTNGuardarEmpleado.setEnabled(false);

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }

// TODO add your handling code here:
    }//GEN-LAST:event_BTNGuardarEmpleadoActionPerformed
private void verificarCamposParaGuardar() {
    boolean completos = !TXTIDEmpleado.getText().isEmpty() &&
                        !TXTNombreEmpleado.getText().isEmpty() &&
                        !TXTApellidoEmpleado.getText().isEmpty() &&
                        !TXTSalario.getText().isEmpty();

 
    if (JCTipoEmpleado.getSelectedItem().equals("Empleado Permanente")) {
        completos = completos && !TXTBeneficio.getText().isEmpty();
    }


    if (JCTipoEmpleado.getSelectedItem().equals("Empleado Temporal")) {
        completos = completos && JCalendarFinContato.getDate() != null &&
                    !TXTHorasTrabajadas.getText().isEmpty() &&
                    !TXTValorHora.getText().isEmpty();
    }

    BTNGuardarEmpleado.setEnabled(completos);
}
