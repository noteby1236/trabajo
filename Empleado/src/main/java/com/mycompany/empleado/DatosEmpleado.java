    private void BTNActualizar_EmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNActualizar_EmpleadoActionPerformed
                                                     
    try {
        int id = Integer.parseInt(TXTIDEmpleado.getText());
        String nombre = TXTNombreEmpleado.getText();
        String apellido = TXTApellidoEmpleado.getText();
        double salario = Double.parseDouble(TXTSalario.getText());
        String tipo = (String) JCTipoEmpleado.getSelectedItem();

     
        for (EmpleadoPermanente emp : empleadoP) {
            if (emp.getIdEmpleado() == id) {
                emp.setNombre(nombre);
                emp.setApellido(apellido);
                emp.setSalario(salario);

                if ("Empleado Permanente".equals(tipo)) {
                    emp.setBeneficio(TXTBeneficio.getText());
                } else {
               
                    empleadoP.remove(emp);
                    EmpleadoTemporal nuevo = new EmpleadoTemporal(
                        id,
                        nombre,
                        apellido,
                        jDateChooser1.getDate(), 
                        salario,
                        JCalendarFinContato.getDate(), 
                        Integer.parseInt(TXTHorasTrabajadas.getText()),
                        Double.parseDouble(TXTValorHora.getText())
                    );
                    empleadoT.add(nuevo);
                }

                javax.swing.JOptionPane.showMessageDialog(this, "Empleado actualizado");
                return;
            }
        }

   
        for (EmpleadoTemporal emp : empleadoT) {
            if (emp.getIdEmpleado() == id) {
                emp.setNombre(nombre);
                emp.setApellido(apellido);
                emp.setSalario(salario);

                if ("Empleado Temporal".equals(tipo)) {
                    emp.setFechaInicioContrato(jDateChooser1.getDate());
                    emp.setFechaFinContrato(JCalendarFinContato.getDate());
                    emp.setHorasTrabajadas(Integer.parseInt(TXTHorasTrabajadas.getText()));
                    emp.setValorHora(Double.parseDouble(TXTValorHora.getText()));
                } else {
                    
                    empleadoT.remove(emp);
               EmpleadoPermanente nuevo = new EmpleadoPermanente(
                 id,
                 nombre,
                 apellido,
                 jDateChooser1.getDate(), 
                 salario,
                 TXTBeneficio.getText()
);

                javax.swing.JOptionPane.showMessageDialog(this, "Empleado actualizado");
                return;
            }
        }
        
        javax.swing.JOptionPane.showMessageDialog(this, "Empleado no encontrado");
        
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al actualizar empleado: " + e.getMessage());
    }

    }//GEN-LAST:event_BTNActualizar_EmpleadoActionPerformed

