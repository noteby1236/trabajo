    private void buscarEmpleadoParaEliminar() {
    int id = Integer.parseInt(TXTIDEmpleado.getText());

    for (EmpleadoPermanente emp : empleadoP) {
        if (emp.getIdEmpleado() == id) {
            mostrarCamposEmpleadoSoloLectura();
            TXTNombreEmpleado.setText(emp.getNombre());
            TXTApellidoEmpleado.setText(emp.getApellido());
            TXTSalario.setText(String.valueOf(emp.getSalario()));
            JCTipoEmpleado.setSelectedItem("Empleado Permanente");
            TXTBeneficio.setText(emp.getBeneficio());
            BTNActualizar_Empleado.setEnabled(true);
            return;
        }
    }

    for (EmpleadoTemporal emp : empleadoT) {
        if (emp.getIdEmpleado() == id) {
            mostrarCamposEmpleadoSoloLectura();
            TXTNombreEmpleado.setText(emp.getNombre());
            TXTApellidoEmpleado.setText(emp.getApellido());
            TXTSalario.setText(String.valueOf(emp.getSalario()));
            JCTipoEmpleado.setSelectedItem("Empleado Temporal");
            JCalendarFinContato.setDate(emp.getFechaFinContrato());
            TXTHorasTrabajadas.setText(String.valueOf(emp.getHorasTrabajadas()));
            TXTValorHora.setText(String.valueOf(emp.getValorHora()));
            BTNActualizar_Empleado.setEnabled(true);
            BTNActualizar_Empleado.setEnabled(true);
            return;
        }
    }

    javax.swing.JOptionPane.showMessageDialog(this, "Empleado no encontrado");
}
