    private void BTNActualizar_DepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNActualizar_DepartamentoActionPerformed
     try {
        int id = Integer.parseInt(TXTIDDepartamento.getText());
        String nombre = TXTNombreDepartamento.getText();
        double salarioBase = Double.parseDouble(TXTSalarioBase.getText());

        for (Departamento depto : departamentos) {
            if (depto.getIdDepartamento() == id) {
                depto.setNombre(nombre);
                depto.setSalarioBase(salarioBase);

                javax.swing.JOptionPane.showMessageDialog(this, "Departamento actualizado");
                limpiarCamposDepartamento();
                BTNActualizar_Departamento.setEnabled(false);
                return;
            }
        }

        javax.swing.JOptionPane.showMessageDialog(this, "Departamento no encontrado");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al actualizar departamento: " + e.getMessage());
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_BTNActualizar_DepartamentoActionPerformed

