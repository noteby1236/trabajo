private void limpiarCamposDepartamento() {
    TXTIDDepartamento.setText("");
    TXTNombreDepartamento.setText("");
    TXTSalarioBase.setText("");
}
private void verificarCamposDepartamento() {
    boolean completos = !TXTIDDepartamento.getText().isEmpty() &&
                        !TXTNombreDepartamento.getText().isEmpty() &&
                        !TXTSalarioBase.getText().isEmpty();

    BTNGuardarDepartamento.setEnabled(completos);
}
private void buscarDepartamentoPorID() {
    int id = Integer.parseInt(TXTIDDepartamento.getText());

    for (Departamento depto : departamentos) {
        if (depto.getIdDepartamento() == id) {
            // Mostrar los demás campos
            TXTNombreDepartamento.setVisible(true);
            TXTSalarioBase.setVisible(true);
            LBLNombre.setVisible(true);
            LBLSalarioBase.setVisible(true);

            // Cargar datos
            TXTNombreDepartamento.setText(depto.getNombre());
            TXTSalarioBase.setText(String.valueOf(depto.getSalarioBase()));

            // Habilitar botón actualizar
            BTNActualizar_Departamento.setEnabled(true);
            return;
        }
    }

    javax.swing.JOptionPane.showMessageDialog(this, "Departamento no encontrado");
}
private void buscarDepartamentoParaEliminar() {
    int id = Integer.parseInt(TXTIDDepartamento.getText());

    for (Departamento depto : departamentos) {
        if (depto.getIdDepartamento() == id) {
            // Mostrar los demás campos en modo lectura
            TXTNombreDepartamento.setVisible(true);
            TXTSalarioBase.setVisible(true);
            LBLNombre.setVisible(true);
            LBLSalarioBase.setVisible(true);

            TXTNombreDepartamento.setText(depto.getNombre());
            TXTSalarioBase.setText(String.valueOf(depto.getSalarioBase()));

            // Deshabilitar edición
            TXTNombreDepartamento.setEditable(false);
            TXTSalarioBase.setEditable(false);

            // Habilitar botón eliminar
            BTNEliminar_Departamento.setEnabled(true);
            return;
        }
    }

    javax.swing.JOptionPane.showMessageDialog(this, "Departamento no encontrado");
}

